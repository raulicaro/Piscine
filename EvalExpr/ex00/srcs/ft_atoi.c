#include "../includes/ft_evalexpr.h"

int		ft_atoi(void)
{
	int result;

	result = ft_next() - '0';
	while (ft_gets() >= '0' && ft_gets() <= '9')
	{
		result = 10 * result + ft_next() - '0';
	}
	return (result);
}
